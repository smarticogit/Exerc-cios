let paises = ["brasil","africa", "italia", "peru", "canada"]
console.log(paises)

paises.push("Irlanda")  // Insere um elento na últimia posição do array
console.log(paises)

paises.pop() // Remove o último elemento do array
console.log(paises)

paises.unshift("EUA") // Insere um elemento na primeira posição do array
console.log(paises)

paises.shift() // Remove a primeira posição do array
console.log(paises)

console.log(paises[paises.length-1]) //Imprime na tela o último elemento do array (Tamanho do array (5) - 1) Índice 4

console.log(paises[1]) // Imprime o segundo item do array

console.log(paises[2]) // Imprime o item de índice 2 (3 posição)
